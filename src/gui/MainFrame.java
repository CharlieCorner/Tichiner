    private void tweetFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tweetFieldKeyTyped
        mainMsgLabel.setText("");
        mainMsgLabel.setBackground(Color.WHITE);

        if (evt.getKeyChar() == java.awt.event.KeyEvent.VK_ENTER) {
            postTweet();
        }

        String text = tweetField.getText();

        if (text.equals("")) {
            mainCharLimit.setText("0");
        } else {
            if (text.length() > 130 && text.length() <= 140) {
                tweetField.setBackground(Color.YELLOW);
            } else if (text.length() > 140) {
                tweetField.setBackground(Color.RED);
            } else {
                tweetField.setBackground(Color.WHITE);
            }
            mainCharLimit.setText(String.valueOf(text.length()));
        }
    }//GEN-LAST:event_tweetFieldKeyTyped

